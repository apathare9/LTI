// program to get the character at the given index within the String

public class _02CharacterAtIndex {
    public static void main (String[] args) {

        String str = "Tutor Joes";
		System.out.println("Given String : " + str);

        char a = str.charAt(0);
        char b = str.charAt(6);

        System.out.println("The Character at Position 0 is " +(char)a);           
		System.out.println("The Character at Position 6 is " +(char)b);





    }
}